# 7-Тапшырма: Саптар (Strings)

# 1. Сап түзүү жана басып чыгаруу
text = "Салам дүйнө!"
print("1-мисал:", text)

# 2. Саптын узундугун табуу
print("2-мисал: Узундугу =", len(text))

# 3. Баш тамга менен жазуу
print("3-мисал:", text.upper())

# 4. Кичине тамга менен жазуу
print("4-мисал:", text.lower())

# 5. Саптын бөлүгүн алуу (слайс)
print("5-мисал:", text[0:5])

# 6. Сапка кошуу
name = "Жумагүл"
greeting = "Салам, " + name + "!"
print("6-мисал:", greeting)

# 7. f-string колдонуу
age = 25
info = f"{name} {age} жашта"
print("7-мисал:", info)

# 8. Сап ичинде сөздү алмаштыруу
new_text = text.replace("дүйно", "Дүйнө")
print("8-мисал:", new_text)

# 9. Сап бошпу же жокпу текшерүү
empty_str = ""
print("9-мисал:", empty_str == "")

# 10. Саптын ичинде белгилүү бир символ барбы?
print("10-мисал:", "д" in text)
